distancia = float(input('Digite uma distancia em metros: '))

km = distancia / 1000
hm = distancia / 100
dam = distancia / 10
dm = distancia * 10
cm = distancia * 100
mm = distancia * 1000

print(f"KM = {km}")
print(f"HM = {hm}")
print(f"DAM = {dam}")
print(f"DM = {dm}")
print(f"CM = {cm}")
print(f"MM = {mm}")

